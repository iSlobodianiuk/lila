/**
 * Системний промпт провідника; {playerRequest} підставляється після фіксації.
 */
export const LEELA_AI_SYSTEM_PROMPT = `
Ти — провідник гри Ліла. Твоя роль — допомагати гравцю знаходити особисті інсайти через гру.

ТВІЙ СТИЛЬ:
- Коучинговий, а не езотеричний
- Ставиш питання, а не даєш відповіді
- Говориш просто і заземлено
- Теплий і людяний тон
- Не використовуєш слова: "вібрації", "всесвіт відповідає", "енергія притягує" тощо

ТВОЇ ІНСТРУМЕНТИ:
- Відкриті питання (Що? Як? Де? Коли? Навіщо?)
- Рефреймінг (побачити ситуацію під іншим кутом)
- Калібруючі питання (за шкалою 1-10)
- Питання на усвідомлення (Що це для тебе означає?)

КОНТЕКСТ ГРИ ЛІЛА:
- 72 клітинки — 72 стани свідомості
- Стріли піднімають, змії опускають
- Кожна клітинка — дзеркало поточного стану гравця
- Мета — не "виграти", а отримати інсайт і план дій

Запит гравця на цю гру: {playerRequest}

ПАМ'ЯТАЙ: Ти не даєш порад — ти ставиш питання, які допомагають гравцю самому знайти відповідь.
`.trim();

export function fillSystemPrompt(fixedRequest: string): string {
  return LEELA_AI_SYSTEM_PROMPT.replace("{playerRequest}", fixedRequest || "— (ще уточнюється)");
}

export function buildEntryUserMessage(roll: number, queryDraft: string): string {
  const q = queryDraft.trim() || "— (порожньо)";

  if (roll >= 1 && roll <= 2) {
    return `Гравець формулює свій запит для гри Ліла. Він кинув кубик і випало ${roll} — це говорить про те, що запит ще дуже розмитий і нечіткий. Постав одне коучингове запитання, щоб допомогти йому краще зрозуміти, чого він насправді хоче від цієї гри. Запит гравця: '${q}'. Будь конкретним і заземленим, без езотерики. Відповідь — лише твоє питання та коротке вітання, без зайвого тексту.`;
  }
  if (roll >= 3 && roll <= 4) {
    return `Гравець формулює запит для гри Ліла. Кубик показав ${roll} — запит стає чіткішим, але ще потребує уточнення. Постав одне питання, яке допоможе гравцю конкретизувати свій запит або знайти справжню причину за поверхневим формулюванням. Запит: '${q}'. Відповідь — лише твоє питання, без зайвого.`;
  }
  if (roll === 5) {
    return `Гравець майже сформулював запит для гри Ліла. Кубик показав 5 — зовсім близько. Постав одне питання, яке допоможе зробити запит максимально чітким і конкретним. Запит: '${q}'. Відповідь — лише твоє питання.`;
  }
  // roll === 6
  return `Гравець сформулював свій запит і кубик показав 6 — запит прийнято! Привітай гравця і підтверди його запит: '${q}'. Скажи, що тепер гра починається і ти будеш поруч на кожному кроці як провідник. Будь теплим і надихаючим, без езотерики.`;
}

const FINAL_MARK_SUMMARY = "===ВИСНОВОК===";
const FINAL_MARK_PLAN = "===ПЛАН_ДІЙ===";

/**
 * Промпт фінального висновку (клітинка 68, кінець гри).
 * Модель має обгорнути блоки в маркери для парсингу.
 */
export function buildFinalSummaryPrompt(input: {
  playerRequest: string;
  gameHistory: { cellNumber: number; cellName: string }[];
  chatHistoryText: string;
}): string {
  const path = input.gameHistory.map((c) => `${c.cellNumber}. ${c.cellName}`).join(" → ");
  return `
Ти — провідник гри Ліла. Гра завершена. Гравець дійшов до клітинки 68 "Космічна свідомість".

Запит гравця: "${input.playerRequest.replace(/"/g, "'")}"

Хід гри (клітинки в порядку відвідування): ${path}

Переписка під час гри:
${input.chatHistoryText}

Зроби:
1. ВИСНОВОК (3-5 речень): Які ключові теми проходили через гру? Що повторювалось? Який головний інсайт?
2. ПЛАН ДІЙ (5 конкретних кроків): Що гравцю варто зробити в найближчі 2 тижні, щоб реалізувати запит?

Стиль: конкретний, заземлений, практичний. Жодної езотерики.

Обов'язково оформи відповідь ТІЛЬКИ у такому форматі (з маркерами, без тексту до або пізніше):
${FINAL_MARK_SUMMARY}
(твій висновок тут)
${FINAL_MARK_PLAN}
(твій план тут, пронумеруй кроки 1–5)
`.trim();
}

export function parseFinalModelOutput(raw: string): { finalSummary: string; actionPlan: string } {
  const i1 = raw.indexOf(FINAL_MARK_SUMMARY);
  const i2 = raw.indexOf(FINAL_MARK_PLAN);
  if (i1 === -1 || i2 === -1) {
    const half = Math.max(1, Math.floor(raw.length / 2));
    return {
      finalSummary: raw.slice(0, half).trim() || raw.trim(),
      actionPlan: raw.slice(half).trim() || "—",
    };
  }
  const after1 = i1 + FINAL_MARK_SUMMARY.length;
  const summary = raw.slice(after1, i2).trim();
  const after2 = i2 + FINAL_MARK_PLAN.length;
  const plan = raw.slice(after2).trim();
  return {
    finalSummary: summary || "—",
    actionPlan: plan || "—",
  };
}

export function formatChatHistoryForFinal(
  items: { role: string; content: string; timestamp: string }[],
): string {
  return items
    .map((m) => `[${m.timestamp}] ${m.role}: ${m.content}`)
    .join("\n\n");
}

